public class Square implements Fillable, Drawable {

	@Override
	public void drawingColor() {
		System.out.println("This is the drawing color of the square");
		
	}

	@Override
	public void thickness() {
		System.out.println("This is the thickness of the square");
		
	}

	@Override
	public void fillingColor() {
		System.out.println("This is the filling color of the square");
		
	}

	@Override
	public void size() {
		System.out.println("This is the size  of the square");
		
	}

}
